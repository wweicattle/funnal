    checked(newVal) {
      let obj = { index: 3, val: newVal };
      this.editPolicy(obj);
      if (newVal && this.userData.urlData.id == 0)
        return (this.AcceptName = this.userData.userInfo.username);
      this.AcceptName = '';
    }
  },
  computed: {
    ...mapState(['policyExist', 'userData'])
  }
